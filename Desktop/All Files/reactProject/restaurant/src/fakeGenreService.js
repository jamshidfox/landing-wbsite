export function getHousesId(id) {
  return houses.find((m) => m._id === id);
}

export function saveMovie(house) {
  let movieInDb = houses.find((m) => m._id === house._id) || {};
  movieInDb.name = house.name;
  // movieInDb.genre = genresAPI.genres.find((g) => g._id === movie.genreId);
  movieInDb.numberInStock = house.numberInStock;
  movieInDb.dailyRentalRate = house.dailyRentalRate;

  if (!movieInDb._id) {
    movieInDb._id = Date.now();
    houses.push(movieInDb);
  }

  return movieInDb;
}

export function deleteMovie(id) {
  let movieInDb = houses.find((m) => m._id === id);
  houses.splice(houses.indexOf(movieInDb), 1);
  return movieInDb;
}
